function ErrorComponent({ error, reset }: { error: Error; reset: () => void }) {
  console.error(error);
  const router = useRouter();
  useEffect(() => {
    reportLovableError(error, { boundary: "tanstack_root_error_component" });
  }, [error]);

  return (
    <div className="flex min-h-screen items-center justify-center bg-background px-4">
      <div className="max-w-md text-center">
        <h1 className="text-xl font-semibold tracking-tight text-foreground">
          This page didn't load
        </h1>
        <p className="mt-2 text-sm text-muted-foreground">
          Something went wrong on our end. You can try refreshing or head back home.
        </p>
        <div className="mt-6 flex flex-wrap justify-center gap-2">
          <button
            onClick={() => {
              router.invalidate();
              reset();
            }}
            className="inline-flex items-center justify-center rounded-md bg-primary px-4 py-2 text-sm font-medium text-primary-foreground transition-colors hover:bg-primary/90"
          >
            Try again
          </button>
          <a
            href="/"
            className="inline-flex items-center justify-center rounded-md border border-input bg-background px-4 py-2 text-sm font-medium text-foreground transition-colors hover:bg-accent"
          >
            Go home
          </a>
        </div>
      </div>
    </div>
  );
}

export const Route = createRootRouteWithContext<{ queryClient: QueryClient }>()({
  head: () => ({
    meta: [
      { charSet: "utf-8" },
      { name: "viewport", content: "width=device-width, initial-scale=1" },
      { title: "Narayana Bus Tracker · Smart Bus Tracking for NEC Gudur" },
      { name: "description", content: "Real-time bus tracking, arrival alerts, and route management for students, faculty, drivers, and administrators at Narayana Engineering College, Gudur." },
      { name: "author", content: "Narayana Engineering College, Gudur" },
      { property: "og:title", content: "Narayana Bus Tracker" },
      { property: "og:description", content: "Live bus tracking and route management for Narayana Engineering College, Gudur." },
      { property: "og:type", content: "website" },
      { name: "twitter:card", content: "summary_large_image" },
    ],
    links: [
      {
        rel: "stylesheet",
        href: appCss,
      },
      { rel: "icon", href: "/favicon.ico", type: "image/x-icon" },
    ],
  }),
  shellComponent: RootShell,
  component: RootComponent,
  notFoundComponent: NotFoundComponent,
  errorComponent: ErrorComponent,
});

function RootShell({ children }: { children: ReactNode }) {
  return (
    <html lang="en">
      <head>
        <HeadContent />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();

  useEffect(() => {
    if (typeof window === "undefined") return;
    const onRejection = (e: PromiseRejectionEvent) => {
      const reason = e.reason;
      const err = reason instanceof Error ? reason : new Error(String(reason ?? "Unhandled promise rejection"));
      console.error("[unhandledrejection]", err);
      reportLovableError(err, { boundary: "window_unhandledrejection" });
      // Toast lazily to avoid pulling sonner into SSR path.
      import("sonner").then(({ toast }) => {
        toast.error("Something went wrong. Please try again.");
      }).catch(() => {});
    };
    const onError = (e: ErrorEvent) => {
      const err = e.error instanceof Error ? e.error : new Error(e.message || "Unknown error");
      console.error("[window.error]", err);
      reportLovableError(err, { boundary: "window_error" });
    };
    window.addEventListener("unhandledrejection", onRejection);
    window.addEventListener("error", onError);
    return () => {
      window.removeEventListener("unhandledrejection", onRejection);
      window.removeEventListener("error", onError);
    };
  }, []);

  return (
    <QueryClientProvider client={queryClient}>
      <ThemeProvider>
        {/* Required: nested routes render here. Removing <Outlet /> breaks all child routes. */}
        <OfflineBanner />
        <Outlet />
        <CommandPalette />
        <Toaster />
      </ThemeProvider>
    </QueryClientProvider>
  );
}
